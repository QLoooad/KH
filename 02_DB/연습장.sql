

-- 사번 생성 시쿼스 START 223
CREATE SEQUENCE SQE_EMP_ID
START WITH 223 -- 223 시작
INCREMENT BY 1 -- 1씩 증가
NOCYCLE -- 반복 없음
NOCACHE; -- 미리 만들어둔 번호 없음

-- SQE_EMP_ID.NEXTVAL : 다음 번호 생성
-- SQE_EMP_ID.CURRVAL : 현재 번호 조회

