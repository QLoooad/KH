SELECT EMP_ID ,EMP_NAME ,DEPT_TITLE, HIRE_DATE
FROM(SELECT EMP_ID ,EMP_NAME ,DEPT_TITLE, HIRE_DATE
FROM EMPLOYEE
JOIN DEPARTMENT ON(DEPT_CODE = DEPT_ID)
ORDER BY HIRE_DATE DESC)
WHERE ROWNUM <= 5;


SELECT DEPT_TITLE, "인원 수", SALARY
