

SELECT EMP_NAME 사원명, 
	FLOOR(ABS(HIRE_DATE - SYSDATE)) 근무일수1, 
	FLOOR(SYSDATE - HIRE_DATE) 근무일수2
FROM EMPLOYEE;



SELECT EMP_ID, EMP_NAME, EMAIL, PHONE
FROM EMPLOYEE
WHERE MOD(EMP_ID, 2) = 1;
WHERE SUBSTR(EMP_ID, -1, 1) ;
--SUBSTR(EMP_ID, 1, 1) 앞에서 한개 
--SUBSTR(EMP_ID, -1, 1) IN (1,3,5,7,9); 뒤에서 한개

SELECT *
FROM EMPLOYEE
WHERE ABS((HIRE_DATE - SYSDATE)/365) > 19 ;
--WHERE MONTHS_BETWEEN(SYSDATE, HIRE_DATE) / 12 >=20;
--WHERE EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM HIRE_DATE) >= 20;


SELECT EMP_NAME, EMP_NO, 
	RPAD(SUBSTR(EMP_NO, 1, 8), 14, '*') 주민등록번호,
	REPLACE(EMP_NO, SUBSTR(EMP_NO, 9), '******') 주민등록번호,
	SUBSTR(EMP_NO,1,8) || '******'주민등록번호
FROM EMPLOYEE;



SELECT EMP_NAME, JOB_CODE, 
TO_CHAR((SALARY + (SALARY * NVL(BONUS, 0)))*12, 'L9,999,999,999') "연봉(원)"
FROM EMPLOYEE;



SELECT EMP_ID, EMP_NAME, DEPT_CODE, HIRE_DATE, TO_CHAR(HIRE_DATE, 'YYYY')
FROM EMPLOYEE
WHERE DEPT_CODE IN('D5', 'D9')
AND HIRE_DATE >= '04/01/01' AND HIRE_DATE <= '04/12/31';
--AND EXTRACT (YEAR FROM HIRE_DATE) = 2004;
--AND TO_CHAR(HIRE_DATE, 'YYYY') = '2004';

SELECT EMP_NAME, HIRE_DATE ,
	EXTRACT (DAY FROM LAST_DAY(HIRE_DATE)) - EXTRACT (DAY FROM HIRE_DATE) + 1 
	"입사한 달의 근무 일수"
FROM EMPLOYEE;




