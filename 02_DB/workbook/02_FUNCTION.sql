-- 9. 춘대학교 2000년 이후 입학자 A 시작  
-- 2000년도 이전 학번 학생의 학번과 이름 출력

SELECT STUDENT_NO, STUDENT_NAME 
FROM TB_STUDENT
WHERE STUDENT_NO NOT LIKE 'A%';



-- 10. 학과별 학생수
--	학과번호, 학생수(명)

SELECT  DEPARTMENT_NO, COUNT(DEPARTMENT_NO)
FROM TB_DEPARTMENT
NATURAL JOIN TB_STUDENT
GROUP BY DEPARTMENT_NO
ORDER BY 1;


-- 11. 지도교수 없는 학생 수

