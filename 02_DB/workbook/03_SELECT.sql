FROM TB_STUDENT
WHERE DEPARTMENT_NO = (SELECT DEPARTMENT_NO 
						FROM TB_STUDENT
						WHERE STUDENT_NAME = '최경희');


-- 18. 국어국문학과 중 총 평점이 가장 높은 학생의 이름 학번					

SELECT STUDENT_NO, STUDENT_NAME
FROM (SELECT STUDENT_NAME,STUDENT_NO, AVG(POINT)학점
FROM TB_GRADE
JOIN TB_STUDENT USING(STUDENT_NO)
WHERE CLASS_NO IN (SELECT CLASS_NO 
					FROM TB_CLASS	
					WHERE DEPARTMENT_NO = (SELECT DEPARTMENT_NO 
											FROM TB_DEPARTMENT
											WHERE DEPARTMENT_NAME = '국어국문학과'))
GROUP BY STUDENT_NAME, STUDENT_NO
ORDER BY 학점 DESC)
WHERE ROWNUM = 1;

					
-- 19. '환경조경학과'가 속한 같은 계열의 학과 별 전공과목 평점


SELECT DEPARTMENT_NAME, ROUND(AVG(POINT),1)
FROM TB_DEPARTMENT
LEFT JOIN TB_STUDENT USING(DEPARTMENT_NO) 
LEFT JOIN TB_GRADE USING(STUDENT_NO) 
WHERE CATEGORY = (SELECT CATEGORY
					FROM TB_DEPARTMENT		
					WHERE DEPARTMENT_NAME = '환경조경학과')
GROUP BY DEPARTMENT_NAME
ORDER BY 1;




















































